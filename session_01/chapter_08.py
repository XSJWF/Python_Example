# 整数类型
# 可以表示 整数，负数，0

n1 = 98
n2 = -76
n3 = 0
print(type(n1))
print(type(n2))
print(type(n3))
#  整数可以表示为：二进制、八进制、十进制、十六进制
print("二进制", 0b10101111)  # 以"0b"开头 取值范围：0-1
print("八进制", 0o1764)  # 以"0o"开头 取值范围：0-7
print("十进制", 118)
print("十六进制", 0x1EAF)  # 以"0x"开头 取值范围：0-9 + A-F
