# 算术运算

print(1 + 1)  # 加法
print(1 - 1)  # 减法
print(1 * 2)  # 乘法
print(1 / 2)  # 除法
print(11 // 2)  # 整除运算，结果取整数
print(11 % 2)  # 取模(求余)运算
print(2 ** 2)  # 求幂运算
print(2 ** 3)

print(9 // 4)  # 2
print(-9 // -4)  # 2
print(9 // -4)  # -3,一正一负的运算结果要向下取整

print(9 % -4)  # -3 公式：余数 = 被除数 - 除数 * 商(整除的商)  9-（-4）*（-3）= -3
print(-9 % 4)  # （-9） - 4 * （-3) = 3
