# 赋值运算符：从右往左

i = 3 + 4
print(i)
a = b = c = 20
print(id(a), id(b), id(c))
# id就是内存地址，a,b,c都指向 “20” 的地址(对象)
print("_________________支持参数赋值______________")
a = 20
a += 30
print(a)
a *= 2
print(a)
a -= 10
print(a)
a /= 4
print(a)
a //= 2
print(a)
print("______________________系列解包运算____________________")
a, b, c = 20, 30, 40
# 左右变量个数以及值的个数一定要相同，并且它的赋值顺序是一一对应的
print(a, b, c)
print("_________________________交换变量的值_______________________")
a, b = b, a
print(a, b)
