# 字典的介绍，是Python中内置的数据结构之一，和列表一样是一个可变序列
# 字典是以键值对方式存储数据的，字典是一个无序的序列，每一个值都有与其唯一对应的键（key）值

# 在字典中你第一个放入的元素不一定就在字典的第一个位置上，字典的排序顺序和其他的不同，
# 有专门的叫字典序（利用Hash(key)函数放置,key是不可变的，不可变序列不能执行增、删、改操作）（结合数据结构中的hash表hash函数来理解）

# 使用{}创建字典

scores = {'张三': 100, '李四': 98, '王五': 90}
print(scores)
print(type(scores))

# 使用dict()创建字典

student = dict(name='王八蛋', age=20)
print(student)

# 创建空字典

dic = {}
print(dic)
